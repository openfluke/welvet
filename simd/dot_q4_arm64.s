// float64 q4BlockDot32Neon(float32 *in, uint32 *packed4, float32 scale)
//
// In-register Q4_0 block GEMV (32 weights): unpack PackQ4_0GPU nibbles in NEON,
// widen to float64, FMLA with activations, multiply by block scale once.
// Mirrors amd64 q4BlockDot32Avx2 — no FP32 scratch buffer.
//
// Ops Go's arm64 assembler lacks (MOVI/AND/USHR/SHL/SSHR/ZIP/SSHLL/SCVTF/FCVTL/
// FADD/FADDP/FCVT/FMUL) are WORD-encoded; encodings verified via clang+otool.
TEXT ·q4BlockDot32Neon(SB), NOSPLIT, $0-32
	MOVD  in+0(FP), R0
	MOVD  packed4+8(FP), R1
	FMOVS scale+16(FP), F15 // keep scale in S15 — V31 is the nibble mask

	VLD1 (R1), [V0.B16] // 4×uint32 = 16 packed nibbles → 32 qs

	WORD $0x4f00e5ff // MOVI V31.16B, #0x0f
	WORD $0x4e3f1c01 // AND  V1.16B, V0.16B, V31.16B   // low nibbles
	WORD $0x6f0c0402 // USHR V2.16B, V0.16B, #4
	WORD $0x4e3f1c42 // AND  V2.16B, V2.16B, V31.16B   // high nibbles
	WORD $0x4f0c5421 // SHL  V1.16B, V1.16B, #4
	WORD $0x4f0c0421 // SSHR V1.16B, V1.16B, #4         // sign-extend −8..7
	WORD $0x4f0c5442 // SHL  V2.16B, V2.16B, #4
	WORD $0x4f0c0442 // SSHR V2.16B, V2.16B, #4
	WORD $0x4e023823 // ZIP1 V3.16B, V1.16B, V2.16B     // q0..q15 chronological
	WORD $0x4e027824 // ZIP2 V4.16B, V1.16B, V2.16B     // q16..q31

	VEOR V24.B16, V24.B16, V24.B16
	VEOR V25.B16, V25.B16, V25.B16
	VEOR V26.B16, V26.B16, V26.B16
	VEOR V27.B16, V27.B16, V27.B16

	// ---- qs 0..15 from V3 ----
	WORD $0x0f08a465 // SSHLL  V5.8H,  V3.8B,  #0
	WORD $0x4f08a466 // SSHLL2 V6.8H,  V3.16B, #0
	WORD $0x0f10a4a7 // SSHLL  V7.4S,  V5.4H,  #0
	WORD $0x4f10a4a8 // SSHLL2 V8.4S,  V5.8H,  #0
	WORD $0x0f10a4c9 // SSHLL  V9.4S,  V6.4H,  #0
	WORD $0x4f10a4ca // SSHLL2 V10.4S, V6.8H,  #0
	WORD $0x4e21d8e7 // SCVTF V7.4S,  V7.4S
	WORD $0x4e21d908 // SCVTF V8.4S,  V8.4S
	WORD $0x4e21d929 // SCVTF V9.4S,  V9.4S
	WORD $0x4e21d94a // SCVTF V10.4S, V10.4S

	VLD1.P 32(R0), [V16.S4, V17.S4] // in[0:8]
	WORD $0x0e6178f2 // FCVTL  V18.D2, V7.S2
	WORD $0x4e6178f3 // FCVTL2 V19.D2, V7.S4
	WORD $0x0e617a14 // FCVTL  V20.D2, V16.S2
	WORD $0x4e617a15 // FCVTL2 V21.D2, V16.S4
	VFMLA V18.D2, V20.D2, V24.D2
	VFMLA V19.D2, V21.D2, V25.D2
	WORD $0x0e617912 // FCVTL  V18.D2, V8.S2
	WORD $0x4e617913 // FCVTL2 V19.D2, V8.S4
	WORD $0x0e617a34 // FCVTL  V20.D2, V17.S2
	WORD $0x4e617a35 // FCVTL2 V21.D2, V17.S4
	VFMLA V18.D2, V20.D2, V26.D2
	VFMLA V19.D2, V21.D2, V27.D2

	VLD1.P 32(R0), [V16.S4, V17.S4] // in[8:16]
	WORD $0x0e617932 // FCVTL  V18.D2, V9.S2
	WORD $0x4e617933 // FCVTL2 V19.D2, V9.S4
	WORD $0x0e617a14 // FCVTL  V20.D2, V16.S2
	WORD $0x4e617a15 // FCVTL2 V21.D2, V16.S4
	VFMLA V18.D2, V20.D2, V24.D2
	VFMLA V19.D2, V21.D2, V25.D2
	WORD $0x0e617952 // FCVTL  V18.D2, V10.S2
	WORD $0x4e617953 // FCVTL2 V19.D2, V10.S4
	WORD $0x0e617a34 // FCVTL  V20.D2, V17.S2
	WORD $0x4e617a35 // FCVTL2 V21.D2, V17.S4
	VFMLA V18.D2, V20.D2, V26.D2
	VFMLA V19.D2, V21.D2, V27.D2

	// ---- qs 16..31 from V4 ----
	WORD $0x0f08a485 // SSHLL  V5.8H,  V4.8B,  #0
	WORD $0x4f08a486 // SSHLL2 V6.8H,  V4.16B, #0
	WORD $0x0f10a4a7 // SSHLL  V7.4S,  V5.4H,  #0
	WORD $0x4f10a4a8 // SSHLL2 V8.4S,  V5.8H,  #0
	WORD $0x0f10a4c9 // SSHLL  V9.4S,  V6.4H,  #0
	WORD $0x4f10a4ca // SSHLL2 V10.4S, V6.8H,  #0
	WORD $0x4e21d8e7 // SCVTF V7.4S,  V7.4S
	WORD $0x4e21d908 // SCVTF V8.4S,  V8.4S
	WORD $0x4e21d929 // SCVTF V9.4S,  V9.4S
	WORD $0x4e21d94a // SCVTF V10.4S, V10.4S

	VLD1.P 32(R0), [V16.S4, V17.S4] // in[16:24]
	WORD $0x0e6178f2 // FCVTL  V18.D2, V7.S2
	WORD $0x4e6178f3 // FCVTL2 V19.D2, V7.S4
	WORD $0x0e617a14 // FCVTL  V20.D2, V16.S2
	WORD $0x4e617a15 // FCVTL2 V21.D2, V16.S4
	VFMLA V18.D2, V20.D2, V24.D2
	VFMLA V19.D2, V21.D2, V25.D2
	WORD $0x0e617912 // FCVTL  V18.D2, V8.S2
	WORD $0x4e617913 // FCVTL2 V19.D2, V8.S4
	WORD $0x0e617a34 // FCVTL  V20.D2, V17.S2
	WORD $0x4e617a35 // FCVTL2 V21.D2, V17.S4
	VFMLA V18.D2, V20.D2, V26.D2
	VFMLA V19.D2, V21.D2, V27.D2

	VLD1.P 32(R0), [V16.S4, V17.S4] // in[24:32]
	WORD $0x0e617932 // FCVTL  V18.D2, V9.S2
	WORD $0x4e617933 // FCVTL2 V19.D2, V9.S4
	WORD $0x0e617a14 // FCVTL  V20.D2, V16.S2
	WORD $0x4e617a15 // FCVTL2 V21.D2, V16.S4
	VFMLA V18.D2, V20.D2, V24.D2
	VFMLA V19.D2, V21.D2, V25.D2
	WORD $0x0e617952 // FCVTL  V18.D2, V10.S2
	WORD $0x4e617953 // FCVTL2 V19.D2, V10.S4
	WORD $0x0e617a34 // FCVTL  V20.D2, V17.S2
	WORD $0x4e617a35 // FCVTL2 V21.D2, V17.S4
	VFMLA V18.D2, V20.D2, V26.D2
	VFMLA V19.D2, V21.D2, V27.D2

	// Horizontal reduce → scale * sum(in*q)
	WORD $0x4e7ad700 // FADD  V0.D2, V24.D2, V26.D2
	WORD $0x4e7bd721 // FADD  V1.D2, V25.D2, V27.D2
	WORD $0x4e61d400 // FADD  V0.D2, V0.D2, V1.D2
	WORD $0x7e70d800 // FADDP D0, V0.D2
	WORD $0x1e22c1e1 // FCVT  D1, S15
	WORD $0x1e610800 // FMUL  D0, D0, D1

	FMOVD F0, ret+24(FP)
	RET
